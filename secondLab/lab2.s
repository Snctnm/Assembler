// Согласно варианту вашего задания, вычислить значение функции с использованием ассемблера Apple ARM 64 для знаковых чисел.
.global _main

// Знаковые числа с выводом в консоль
.text
// С остатком и восстановлением стека
.align 2
// Ввод данных из консоли

_main:
    // Выделяем место на стеке
    sub sp, sp, #96                    // Выделяем 96 байт на стеке (3 числа по 8 байт + выравнивание)
    stp x29, x30, [sp, #80]            // Сохраняем fp и lr
    add x29, sp, #80                   // Устанавливаем новый fp

    // Адреса переменных на стеке
    sub x8, x29, #24                   // Адрес для первого числа (8 байт)
    sub x9, x29, #32                   // Адрес для второго числа (8 байт)

    // Передаём адреса переменных через стек
    mov x11, sp                        // Указатель на стек
    str x8, [x11]                      // Сохраняем адрес первого числа на стеке
    str x9, [x11, #8]                  // Сохраняем адрес второго числа на стеке

    // Адрес формата для scanf
    adrp x0, input_str@PAGE            // Вычисляем страницу для формата
    add x0, x0, input_str@PAGEOFF      // Добавляем смещение внутри страницы

    // Вызов scanf
    bl _scanf                          // Вызываем scanf

    // Загружаем значения из памяти
    ldr x8, [x29, #-24]                // Загружаем первое число
    ldr x9, [x29, #-32]                // Загружаем второе число

    // Вычисления: Z = 2 * (A^3) / (5*B^2 + 3*B)  (знаковое)
    
    // B^2
    mul x14, x9, x9            // x14 = B^2
    // 5 * B^2
    mov x15, #5
    mul x14, x14, x15          // x14 = 5*B^2
    // 3 * B
    mov x15, #3
    mul x15, x9, x15           // x15 = 3*B
    // denom = 5*B^2 + 3*B
    add x14, x14, x15          // x14 = denom
    
1:  // A^2
    mul x11, x8, x8            // x11 = A^2
    // A^3
    mul x12, x11, x8           // x12 = A^3
    // numerator = 2 * A^3
    mov x13, #2
    mul x12, x12, x13          // x12 = 2*A^3

    // Деление (знаковое)
    sdiv x2, x12, x14          // x2 = result (частное)
    // Остаток: r = numerator - q * denom
    mul x4, x2, x14
    sub x3, x12, x4            // x3 = remainder

    // Подготовка к выводу
    mov x11, sp
    str x2, [x11]                      // Сохраняем частное в стек
    str x3, [x11, #8]                  // Сохраняем остаток в стек (+8 байт смещение)

    // Вывод результата
    adrp x0, output_str@PAGE           // Загружаем адрес строки в x0
    add x0, x0, output_str@PAGEOFF
    bl _printf                         // Вызываем C-функцию вывода

    // Освобождаем стек и возвращаемся
    ldp x29, x30, [sp, #80]            // Восстанавливаем fp и lr
    add sp, sp, #96                    // Освобождаем стек

    // Завершение программы
    mov x0, #0
    mov X16, #1                        // Системный вызов 1 завершает программу
    svc #0x80                          // Вызываем системную функцию с номером 1

// Секция данных
input_str:
    .asciz "%lld %lld"

output_str:
    .asciz "Output value: %lld,  Modulo:%lld\n"